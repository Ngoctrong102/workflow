package com.notificationplatform.dto.response;

import java.util.List;
import java.util.Map;

public class FieldDefinitionResponseDTO {

    private String name;
    private String displayName;
    private String type;
    private Boolean required;
    private Object defaultValue;
    private Map<String, Object> validation;
    private String description;
    private List<String> examples;
    private String objectTypeId;
    private String itemType;
    private String itemObjectTypeId;

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Boolean getRequired() {
        return required;
    }

    public void setRequired(Boolean required) {
        this.required = required;
    }

    public Object getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(Object defaultValue) {
        this.defaultValue = defaultValue;
    }

    public Map<String, Object> getValidation() {
        return validation;
    }

    public void setValidation(Map<String, Object> validation) {
        this.validation = validation;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<String> getExamples() {
        return examples;
    }

    public void setExamples(List<String> examples) {
        this.examples = examples;
    }

    public String getObjectTypeId() {
        return objectTypeId;
    }

    public void setObjectTypeId(String objectTypeId) {
        this.objectTypeId = objectTypeId;
    }

    public String getItemType() {
        return itemType;
    }

    public void setItemType(String itemType) {
        this.itemType = itemType;
    }

    public String getItemObjectTypeId() {
        return itemObjectTypeId;
    }

    public void setItemObjectTypeId(String itemObjectTypeId) {
        this.itemObjectTypeId = itemObjectTypeId;
    }
}

